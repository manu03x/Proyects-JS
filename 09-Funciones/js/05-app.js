// Parametros y argumentos

function sumar(a,b) { // a y b son parametros
    console.log( a + b );
}

sumar(20,30); // 20 y 30 son argumentos


sumar(25,321); // 25 y 321 son argumentos


function saludar(nombre, apellido){ // Estos son los parametros
    console.log(`Hola ${nombre} ${apellido}`);
}

saludar('Emmanuel', 'Ruiz'); // Estos son argumentos

